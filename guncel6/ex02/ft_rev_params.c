#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	main(int argc, char *argv[])
{
	int	i;
	int	j;

	j = argc - 1;
	while (j > 0)
	{
		i = 0;
		while (argv[j][i] != '\0' && argc)
		{
			ft_putchar (argv[j][i]);
			i++;
		}
		write(1, "\n", 1);
		j--;
	}
	return (0);
}
