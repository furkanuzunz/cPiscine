#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;
	int	d;

	i = 0;
	d = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	while (d < i)
	{
		write (1, str + d, 1);
		d++;
	}
}
